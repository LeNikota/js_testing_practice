export default function capitalize(text = null) {
  if (typeof text !== "string") return null;
  if (!isNaN(+text.charAt())) return null;
  if (text.charAt() === text.charAt().toUpperCase()) return text;
  return text.charAt(0).toUpperCase() + text.slice(1);
}
